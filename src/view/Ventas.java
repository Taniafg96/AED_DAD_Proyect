    private void btnActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarMouseClicked
         VentasCRUD venta = new VentasCRUD();
        if(campoCantidadProducto.getText().matches("^[+-]?([0-9]+([.][0-9]*)?|[.][0-9]+)$")){
            if(!campoCantidadProducto.getText().isEmpty())
            venta.setProductLot(Integer.parseInt(campoCantidadProducto.getText()));
            else JOptionPane.showMessageDialog(null, "Tiene que insertar un valor en "
                + "cantidad de producto", "ERROR", JOptionPane.WARNING_MESSAGE);
        }else JOptionPane.showMessageDialog(null, "Introduca un dato valido en "
            + "el cantidad de producto", "ERROR Tipo", JOptionPane.WARNING_MESSAGE);
        venta.modify(Integer.parseInt(campoIdVenta.getText()));
        
        limpiar();
        mostrarTabla();
    }//GEN-LAST:event_btnActualizarMouseClicked

