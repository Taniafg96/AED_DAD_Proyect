    private void btnInsertarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInsertarMouseClicked
        int cantidadProducto = 0;
        String codProduct = jComboBoxCodigoProducto.getSelectedItem().toString();
        System.out.println(codProduct);
        String IdClient = jComboBoxIDCliente.getSelectedItem().toString();
        String dniUser = this.jComboBoxDniUsuario.getSelectedItem().toString();
        if(campoCantidadProducto.getText().matches("^[+-]?([0-9]+([.][0-9]*)?|[.][0-9]+)$")){
            try {
                cantidadProducto = Integer.parseInt(campoCantidadProducto.getText());
                VentasCRUD venta = new VentasCRUD(cantidadProducto, dniUser, codProduct, IdClient);
                venta.insert();
                limpiar();
                mostrarTabla();
            } catch (NumberFormatException e){
                JOptionPane.showMessageDialog(null, e.toString());
                limpiar();
            }
        }else JOptionPane.showMessageDialog(null, "Introduca un dato valido en "
            + "el cantidad de producto", "ERROR Tipo", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnInsertarMouseClicked

