    private void insertjComboBoxCodigoProdcuto(){
        List<String> codigo = new ArrayList<>();
        try(Connection CONNECT = new ConnectDB().getConetion();
                Statement stm = CONNECT.createStatement();){
            String consult = "SELECT codigo FROM Productos";
            ResultSet rs  = stm.executeQuery(consult);
            
            jComboBoxCodigoProducto.removeAllItems();
            
            while(rs.next()){
                codigo.add(rs.getString("codigo"));
            }
                
            rs.close();
            stm.close();
            CONNECT.close();
        }catch(SQLException ex){
            ex.printStackTrace();
        }
        codigo.forEach((i) -> { jComboBoxCodigoProducto.addItem(i);});
    }
    
